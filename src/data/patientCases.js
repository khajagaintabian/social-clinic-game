export function getAvailablePatients(rankLevel, department = 'All') {
  return patientCases.filter((c) => {
    if (c.minRankLevel > rankLevel) return false;
    if (department !== 'All' && c.department !== department) return false;
    return true;
  });
}

export function hasUnlockedPatients(rankLevel, department = 'All') {
  return getAvailablePatients(rankLevel, department).length > 0;
}

export function getRandomPatient(rankLevel, excludeId = null, department = 'All') {
  let pool = getAvailablePatients(rankLevel, department);
  if (excludeId !== null && pool.length > 1) {
    pool = pool.filter((c) => c.id !== excludeId);
  }
  if (pool.length === 0) return null;
  return pool[Math.floor(Math.random() * pool.length)];
}
