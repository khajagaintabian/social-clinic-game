export function getAvailablePatients(rankLevel) {
  return patientCases.filter((c) => c.minRankLevel <= rankLevel);
}

export function getRandomPatient(rankLevel, excludeId = null) {
  const pool = getAvailablePatients(rankLevel).filter((c) => c.id !== excludeId);
  if (pool.length === 0) return patientCases[0];
  return pool[Math.floor(Math.random() * pool.length)];
}
